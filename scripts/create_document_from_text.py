def simple_parse_to_structure(raw_text: str) -> dict:
    """A tiny heuristic parser that extracts a title, sections and paragraphs.

    - Title is taken from a line starting with `Title:` if present, otherwise 'Imported Document'
    - Sections start with `Section: <title>` lines
    - Paragraphs are groups of non-empty lines separated by blank lines
    """
    title = 'Imported Document'
    lines = [l.rstrip() for l in raw_text.splitlines()]

    sections = []
    current_section = {'title': 'Body', 'order': 0, 'depth_level': 1, 'paragraphs': []}
    para_lines = []
    section_count = 0

    for line in lines:
        if not line.strip():
            # blank line -> end paragraph
            if para_lines:
                paragraph_text = '\n'.join(para_lines).strip()
                current_section['paragraphs'].append({
                    'client_id': f'temp_para_{uuid.uuid4().hex[:8]}',
                    'content_text': paragraph_text,
                    'edited_text': paragraph_text,
                    'order': len(current_section['paragraphs'])
                })
                para_lines = []
            continue

        if line.lower().startswith('title:'):
            title = line.split(':', 1)[1].strip() or title
            continue

        if line.lower().startswith('section:'):
            # flush existing section
            if current_section['paragraphs'] or current_section['title'] != 'Body' or section_count > 0:
                sections.append(current_section)
                section_count += 1

            sec_title = line.split(':', 1)[1].strip() or f'Section {section_count+1}'
            current_section = {
                'title': sec_title,
                'order': section_count,
                'depth_level': 1,
                'paragraphs': []
            }
            para_lines = []
            continue

        # normal content line
        para_lines.append(line)

    # flush last paragraph
    if para_lines:
        paragraph_text = '\n'.join(para_lines).strip()
        current_section['paragraphs'].append({
            'client_id': f'temp_para_{uuid.uuid4().hex[:8]}',
            'content_text': paragraph_text,
            'edited_text': paragraph_text,
            'order': len(current_section['paragraphs'])
        })

    # append last section
    if current_section and (current_section['paragraphs'] or current_section['title'] != 'Body'):
        sections.append(current_section)

    # normalize section client_ids
    for idx, sec in enumerate(sections):
        sec.setdefault('client_id', f'temp_sec_{uuid.uuid4().hex[:8]}')
        sec.setdefault('order', idx)
        sec.setdefault('depth_level', 1)
        for p_idx, p in enumerate(sec.get('paragraphs', [])):
            p.setdefault('client_id', f'temp_para_{uuid.uuid4().hex[:8]}')
            p.setdefault('order', p_idx)

    structure = {
        'title': title,
        'author': None,
        'sections': sections
    }
    return structure


